package com.example.model;

import java.util.List;

public class LayoutCharacteristics {
private int total_booths;
private int main_aisles;
private List<String> special_areas;

// getters and setters  
public int getTotal_booths() {  
    return total_booths;  
}  
public void setTotal_booths(int total_booths) {  
    this.total_booths = total_booths;  
}  
public int getMain_aisles() {  
    return main_aisles;  
}  
public void setMain_aisles(int main_aisles) {  
    this.main_aisles = main_aisles;  
}  
public List<String> getSpecial_areas() {  
    return special_areas;  
}  
public void setSpecial_areas(List<String> special_areas) {  
    this.special_areas = special_areas;  
}  
}
